        // ลบคำถามเก่าทั้งหมดก่อน
        ExamQuestion::query()->delete();

        $parts = ExamPart::orderBy('part_number')->get();

        foreach ($parts as $part) {
            $pre  = $this->preQuestions($part->part_number);
            $post = $this->postQuestions($part->part_number);

            foreach ($pre as $order => $q) {
                ExamQuestion::create([
                    'exam_part_id'  => $part->id,
                    'section'       => 'pre',
                    'question_text' => $q['q'],
                    'choice_1'      => $q['c'][0],
                    'choice_2'      => $q['c'][1],
                    'choice_3'      => $q['c'][2],
                    'choice_4'      => $q['c'][3],
                    'correct_choice'=> $q['a'],
                    'order'         => $order + 1,
                ]);
            }

            foreach ($post as $order => $q) {
                ExamQuestion::create([
                    'exam_part_id'  => $part->id,
                    'section'       => 'post',
                    'question_text' => $q['q'],
                    'choice_1'      => $q['c'][0],
                    'choice_2'      => $q['c'][1],
                    'choice_3'      => $q['c'][2],
                    'choice_4'      => $q['c'][3],
                    'correct_choice'=> $q['a'],
                    'order'         => $order + 1,
                ]);
            }
        }

        $this->command->info('สร้างคำถาม ' . ExamQuestion::count() . ' ข้อ เรียบร้อย');
    }

